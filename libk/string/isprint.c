#include <libk.h>

int isprint(int c) {
	return (c == 32 || isalnum(c) || (c >= 33 && c <= 47)
		|| (c >= 58 && c <= 64) || (c >= 91 && c <= 96)
		|| (c >= 123 && c <= 126));
}